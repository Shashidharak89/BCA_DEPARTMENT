// Get the popup container, popup, and buttons
const popupContainer1 = document.querySelector('.popup-container1');
const popup1 = document.querySelector('.popup1');
const openButton1 = document.getElementById('open-popup1');
const closeButton1 = document.getElementById('close-popup1');

// Add event listeners to the buttons
openButton1.addEventListener('click', () => {
    popupContainer1.style.display = 'flex';
});

closeButton1.addEventListener('click', () => {
    popupContainer1.style.display = 'none';
});



